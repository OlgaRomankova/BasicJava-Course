//While (Условие) {
//    Повторяющиеся операциии, которые будут
//    выполняться столько раз пока условие истинно
//    }  Сциклом while нужно быть аккуратным, чтобы не допустить зацикливания


public class CycleWHILE {
    public static void main(String[] args) {
        int a = 0;
        while  (a <= 18){
         System.out.println(a);
            a++;
        }
    }
}
